    private void btnGraficarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGraficarActionPerformed
        try {
            // TODO add your handling code here:

            ArrayList estudiantes = servicio.getEstudiantes();
            
            DefaultCategoryDataset dts = new DefaultCategoryDataset();
            
            for(int i = 0; i < estudiantes.size(); i++)
            {
                Estudiante estu = (Estudiante) estudiantes.get(i);
                int edad = estu.getEdad();
                
                dts.setValue(edad, estu.getNombre(), estu.getCodigo());
            }
            
            JFreeChart ch = ChartFactory.createBarChart("Gráfica Edad", "Codigo", "Edad", dts, PlotOrientation.VERTICAL,true,true,false);
            ChartPanel cp = new ChartPanel(ch);
            panel.add(cp);
            cp.setVisible(false);
            cp.setVisible(true);
            cp.setBounds(10, 5, 500, 400);
            
        } catch (RemoteException ex) {
            Logger.getLogger(GUIGrafica.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_btnGraficarActionPerformed


